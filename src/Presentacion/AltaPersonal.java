    void cargarTabla(){
        md=new DefaultTableModel(data,columnas);
        this.tabla.setModel(md);
        //lleno la tabla
         List<Personal> per = personaContoler.listarPersonal();
        for(Personal aux : per){ 
            String datos[]={aux.getNombre(),aux.getApellido(),String.valueOf(aux.getCedula())};
            md.addRow(datos);
        }
    }
    
